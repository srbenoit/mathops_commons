    /**
     * Tests whether a color name is valid. Valid names can be either the text name of a color, or a hex triplet in the
     * form RRGGBB. Letters in hex values can be either upper-case or lower-case, but the text color names are
     * case-sensitive.
     *
     * @param name the name of the color
     * @return {@code true} if the name is valid; {@code false} otherwise
     */
    public static boolean isColorNameValid(final String name) {

        boolean valid = indexOfName(name) != -1;

        // If not a preset, see if it's a hex value (RRGGBB)
        if (!valid && name.length() == HEX_COLOR_LEN) {

            try {
                Integer.parseInt(name, HEX);
                valid = true;
            } catch (final NumberFormatException exc) {
                Log.warning(ERR_MSG, name);
            }
        }

        return valid;
    }

    /**
     * Gets a color based on a name or hex triplet.
     *
     * @param name the name or hex value of the color
     * @return the generated {@code Color}, or {@code Color.BLACK} if the name could not be understood
     */
    public static Color getColor(final String name) {

        final int index = indexOfName(name);
        Color color;

        if (index >= 0) {
            color = new Color(RGB[index][0], RGB[index][1], RGB[index][2]);
        } else if (name.length() == HEX_COLOR_LEN) {

            try {
                final int val = Integer.parseInt(name, HEX);
                color = new Color(val >> TWO_BYTES & BYTE_MASK, val >> ONE_BYTE & BYTE_MASK,
                        val & BYTE_MASK);
            } catch (final NumberFormatException exc) {
                Log.warning(ERR_MSG, name);
                color = Color.BLACK;
            }
        } else {
            Log.warning("No color found with name '", name, "'");
            color = Color.BLACK;
        }

        return color;
    }

    /**
     * Looks up the index of a color name in the X11 database.
     *
     * @param name the color name
     * @return the index, or -1 if the name was not found
     */
    private static int indexOfName(final String name) {

        int index = -1;

        if (name != null) {
            final int len = CNAMES.length;
            for (int i = 0; i < len; ++i) {
                if (CNAMES[i].equals(name)) {
                    index = i;
                    break;
                }
            }
        }

        return index;
    }
